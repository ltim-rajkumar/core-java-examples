package com.lt.practice;

public class ObjectDemo {

    /**
     * Object - is a generic class
     * Any custom class extends Object class by default
     * We can store any type of value in object class
     */
    public static void main(String[] args) {
        Object objectName = "Rajkumar";
        Object objectMobileNo = 9090;
        Object objectYearOfExperience = 10.1f;
    }
}
